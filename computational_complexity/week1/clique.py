def clique(g, k):
    if k == 1:
        return [0]

    if clique_problem(g, k) == 1:
        cliques = [{i, j} for i in range(len(g) - 1) for j in range(i + 1, len(g)) if i != j and g[i][j] == 1]
        current_k = 2

        while current_k < k:
            clique_1 = set()
            combinations = itertools.combinations(cliques, 2)

            cliques = []

            for u, v in combinations:
                w = u ^ v
                if len(w) == 2:
                    i, j = w
                    if g[i][j] == 1:
                        t = tuple(w | u)
                        if t not in clique_1:
                            cliques.append(w | u)

                            if current_k == k-1:
                                break
                        clique_1.add(t)

            current_k += 1

        return list(cliques[0]) if len(cliques) > 0 else []

    return []

